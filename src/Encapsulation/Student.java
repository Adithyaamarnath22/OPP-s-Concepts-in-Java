package Encapsulation;

public class Student {
String name;
String Id;
String dep;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getId() {
	return Id;
}
public void setId(String string) {
	Id = string;
}
public String getDep() {
	return dep;
}
public void setDep(String dep) {
	this.dep = dep;
}

	
}

